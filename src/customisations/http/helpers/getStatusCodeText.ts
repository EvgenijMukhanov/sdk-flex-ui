/**
 * Возвращает текстовое описание HTTP-кода состояния
 */
export const getStatusCodeText = (status: number): string => {
  const statusTexts: Record<number, string> = {
    400: 'Неверный запрос',
    401: 'Не авторизован',
    402: 'Требуется оплата',
    403: 'Доступ запрещён',
    404: 'Не найдено',
    405: 'Метод не разрешён',
    408: 'Превышено время ожидания',
    409: 'Конфликт',
    410: 'Ресурс удалён',
    413: 'Слишком большой запрос',
    414: 'Слишком длинный URL',
    415: 'Неподдерживаемый тип контента',
    429: 'Слишком много запросов',
    500: 'Внутренняя ошибка сервера',
    501: 'Метод не поддерживается',
    502: 'Неверный шлюз',
    503: 'Сервис недоступен',
    504: 'Шлюз не отвечает',
  };

  if (status >= 100 && status < 200) {
    return 'Информационный';
  }
  if (status >= 300 && status < 400) {
    return 'Перенаправление';
  }

  return statusTexts[status] || `Ошибка ${status}`;
};
